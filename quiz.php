foreach($answers as $answer) {//Το foreach είναι για το πέρασμα ένα-ένα των στοιχείων του πίνακα
	if ($answer==$correct[$i]) {//Σε αυτό το σημείο θα γίνει έλεγχος αν η απάντηση που έδωσε ο χρήστης είναι σωστή ή όχι
		$score++;//Εάν η απάντηση είναι σωστή, τότε το σκορ θα αυξηθεί κατά 1
	}
